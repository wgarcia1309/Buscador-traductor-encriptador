    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        if (!(EBE.isSelected()) && !(EBI.isSelected())) {
            JOptionPane.showMessageDialog(this, "Seleccione una opcion");
        } else {
            String del;
            del = jTextField1.getText();
            if (buscar(del, EE,0) != -1  || (buscar(del, II,0))!=-1) { 
               if (EBE.isSelected()) {
                    deleteEE(del);
                } else {
                    deleteII(del);                    
                }
                act--;
                JOptionPane.showMessageDialog(this, "Palabra eliminada");
                Dc v1 = new Dc();
                v1.setVisible(true);
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(this, "La palabra no se encuentra");
            }
        }
    }//GEN-LAST:event_DeleteActionPerformed

